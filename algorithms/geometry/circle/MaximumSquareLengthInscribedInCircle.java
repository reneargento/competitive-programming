// Computes the maximum length of a square that can be inscribed in a circle of radius circleRadius.
public class MaximumSquareLengthInscribedInCircle {

    private static double maximumSquareLengthInscribedInCircle(double circleRadius) {
        return Math.sqrt(2) * circleRadius;
    }

}
